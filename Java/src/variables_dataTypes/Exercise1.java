package variables_dataTypes;

public class Exercise1 {

	public static void main(String[] args) {
		 
		int a = 1234;
		int b = 99;
		
		System.out.println(" A = " + a + ", B = " + b);
		System.out.println(" A + B = " + (a+b));
		System.out.println(" A - B = " + (a-b));
		System.out.println(" A * B = " + (a*b));
		System.out.println(" A / B = " + (a/b));
		System.out.println(" A % B = " + (a%b));
	}

}
